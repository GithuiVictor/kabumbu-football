    public void populateSchools() {

        Vector<String> columnNames = new Vector<>();
        columnNames.add("School Id");
        columnNames.add("School Name");
        columnNames.add("P.O. Box");
        columnNames.add("Postal Code");
        columnNames.add("City");
        columnNames.add("Amount paid");
        
        Vector<Vector<Object>> data = new Vector<>();
        
        String sql = "SELECT * FROM schools;";
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/kambumbufootballleagemanagementsystem", "root", "");
                Statement stmt = conn.createStatement()) {
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                Vector<Object> row = new Vector<>();
                
                row.add(rs.getString("schoolid"));
                row.add(rs.getString("schoolname"));
                row.add(rs.getString("pobox"));
                row.add(rs.getString("postalcode"));
                row.add(rs.getString("city"));
                row.add(rs.getString("amountpaid"));
                
                data.add(row);
                
            }
            schoolsTable.setModel(new DefaultTableModel(data, columnNames){
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            });
            
        } catch (SQLException ex) {
            Logger.getLogger(CoachRegestrationForm.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }
    }

