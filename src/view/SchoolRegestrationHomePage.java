    private void deleRecordBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleRecordBtnActionPerformed
        // TODO add your handling code here:
        int row = schoolsTable.getSelectedRow();
        int schoolid = Integer.parseInt(schoolsTable.getValueAt(row, 0).toString());
        
         String sql = "DELETE FROM schools WHERE schoolid = ?";
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/kambumbufootballleagemanagementsystem", "root", "");
                PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setInt(1, schoolid);
           
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Successfully Deleted!!");
            populateSchools();
          

        } catch (SQLException ex) {
            Logger.getLogger(KabumbuFootballLeage.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_deleRecordBtnActionPerformed

    private void schoolsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_schoolsTableMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            JTable source = (JTable) evt.getSource();
            int row = source.rowAtPoint(evt.getPoint());
            
            Common.schoolid = Integer.parseInt(source.getModel().getValueAt(row, 0).toString());
            
            KabumbuFootballLeage.scup.populateSchoolUpdate();
                    
            KabumbuFootballLeage.scup.setVisible(true);
        }
    }//GEN-LAST:event_schoolsTableMouseClicked

