    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        String sql = "UPDATE coach "
                + "SET firstName = ?, lastName = ?, schoolid = ?, dateOfBirth = ?, phoneNumber = ?, email = ?, schoolCoaching = ?, medicalIssue = ?, explanation = ? "
                + "WHERE id = ?";
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/kambumbufootballleagemanagementsystem", "root", "");
                PreparedStatement pstmt = conn.prepareStatement(sql)) {            
            String fName = firstNameTf.getText();
            String lName = lastNameTf.getText();
            String date =dateTf.getText();
            int schoolid = Integer.parseInt(schoolIdTf.getText());
            int phoneNumber = Integer.parseInt(phoneNumberTf.getText());
            String emailAddress = emailAddressTf.getText();
            String schoolCOaching = jComboBox1.getSelectedItem().toString();
            String medicalIssue;
            if (yesRBtn.isSelected()){
                medicalIssue = "Yes";
            } else {
                medicalIssue = "No";
            }
            String explanation = explanationTf.getText();
            
            
            
            pstmt.setString(1, fName);
            pstmt.setString(2, lName);
            pstmt.setInt(3, schoolid);
            pstmt.setString(4, date);
            pstmt.setInt(5, phoneNumber);
            pstmt.setString(6, emailAddress);
            pstmt.setString(7, schoolCOaching);
            pstmt.setString(8, medicalIssue);
            pstmt.setString(9, explanation);
            pstmt.setInt(10, Common.coachId);
            
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Successfully !");
            setVisible(false);
            KabumbuFootballLeage.crhp.populateCoach();
            KabumbuFootballLeage.crhp.setVisible(true);

        } catch (SQLException ex) {
            Logger.getLogger(KabumbuFootballLeage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

