    public void populateCoachUpdate() {
        String sql = "SELECT coach.*, schools.schoolname FROM coach, schools WHERE  schools.schoolid = coach.schoolid AND coach.id = ?;";
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/kambumbufootballleagemanagementsystem", "root", "");
                PreparedStatement stmt = conn.prepareStatement(sql)) {
            stmt.setInt(1, Common.coachId);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                firstNameTf.setText(rs.getString("firstName"));
                lastNameTf.setText(rs.getString("lastName"));
                dateTf.setText(rs.getDate("dateOfBirth") + "");
                schoolIdTf.setText(rs.getString("schoolid"));
                phoneNumberTf.setText(rs.getInt("phoneNumber") + "");
                emailAddressTf.setText(rs.getString("email"));
                jComboBox1.setSelectedItem(rs.getString("schoolname"));
                if (rs.getString("medicalIssue").contentEquals("YES")) {
                    yesRBtn.setSelected(true);
                    noRBtn.setSelected(false);
                } else {
                    yesRBtn.setSelected(false);
                    noRBtn.setSelected(true);
                }
                explanationTf.setText(rs.getString("explanation"));

            }

        } catch (SQLException ex) {
            Logger.getLogger(CoachUpdate.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }
    }

