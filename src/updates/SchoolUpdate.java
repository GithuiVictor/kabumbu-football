    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        String sql = "UPDATE schools "
                + "SET schoolname = ?, schoolid = ?, pobox = ?, postalcode = ?, city = ?, amountpaid = ? "
                + "WHERE schoolid = ?";
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/kambumbufootballleagemanagementsystem", "root", "");
                PreparedStatement pstmt = conn.prepareStatement(sql)) {            
            String schoolName = schoolNameTf.getText();
            int schoolid = Integer.parseInt(schoolIdTf.getText());
            int poBox = Integer.parseInt(poBoxTf.getText());
            int postalCode = Integer.parseInt(postalCodeTf.getText());
            String city = cityTf.getText();
            int amountPaid = Integer.parseInt(amountPaidTf.getText());
            
            
            pstmt.setString(1, schoolName);
            pstmt.setInt(2, schoolid);
            pstmt.setInt(3, poBox);
            pstmt.setInt(4, postalCode);
            pstmt.setString(5, city);
            pstmt.setInt(6, amountPaid);
            pstmt.setInt(7, Common.schoolid);
            
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Successfully !");
            setVisible(false);
            KabumbuFootballLeage.srhp.populateSchools();
            KabumbuFootballLeage.srhp.setVisible(true);

        } catch (SQLException ex) {
            Logger.getLogger(KabumbuFootballLeage.class.getName()).log(Level.SEVERE, null, ex);
        }




    }//GEN-LAST:event_updateBtnActionPerformed

