    public void populateSchoolUpdate() {
        String sql = "SELECT * FROM schools WHERE schoolid = ?;";
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/kambumbufootballleagemanagementsystem", "root", "");
                PreparedStatement stmt = conn.prepareStatement(sql)) {
            stmt.setInt(1, Common.schoolid);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                schoolNameTf.setText(rs.getString("schoolname"));
                schoolIdTf.setText(rs.getInt("schoolid") +"");
                poBoxTf.setText(rs.getInt("pobox") +"");
                postalCodeTf.setText(rs.getInt("postalcode") + "");
                cityTf.setText(rs.getString("city"));
                amountPaidTf.setText(rs.getInt("amountpaid") +"");
            }

        } catch (SQLException ex) {
            Logger.getLogger(SchoolUpdate.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }
    }

